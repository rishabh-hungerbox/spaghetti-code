from django.db import models


class OrderItems(models.Model):
    order_item_id = models.AutoField(primary_key=True)
    order_id = models.IntegerField()
    product_id = models.IntegerField()
    price = models.FloatField()
    qty = models.IntegerField()
    created_at = models.DateTimeField(blank=True, null=True, auto_now_add=True)
    updated_at = models.DateTimeField(blank=True, null=True, auto_now=True)
    product_name = models.CharField(max_length=255)
    company_price = models.FloatField(default=0)
    employee_paid = models.FloatField(default=0)
    subsidy_value = models.FloatField(default=0)
    # hb_get = models.FloatField()
    item_price = models.FloatField()
    is_free = models.IntegerField(blank=True, null=True)
    is_mrp = models.IntegerField()
    recommendation_type = models.CharField(max_length=255, blank=True, null=True)
    recommendation_id = models.IntegerField(blank=True, null=True)
    recommendation_score = models.FloatField(blank=True, null=True)
    convenience_fee = models.DecimalField(max_digits=6, decimal_places=2)
    status = models.CharField(max_length=40, null=True)
    estimated_delivery_time = models.DateTimeField(null=True)
    processed_time = models.DateTimeField(null=True)
    delivery_time = models.DateTimeField(null=True)
    comment = models.CharField(max_length=255, blank=True, null=True, default=None)
    container_charge = models.DecimalField(max_digits=6, decimal_places=2, default=0)
    reject_message = models.CharField(max_length=20, blank=True, null=True)
    item_price_incl_gst = models.DecimalField(max_digits=14, decimal_places=2, default=0)
    cgst = models.DecimalField(max_digits=7, decimal_places=2, default=0)
    sgst = models.DecimalField(max_digits=7, decimal_places=2, default=0)
    hsn_code_id = models.IntegerField(default=None)
    invoice_no = models.CharField(max_length=20, blank=True, null=True)
    invoice_type = models.CharField(max_length=20, blank=True, null=True)
    cess = models.DecimalField(max_digits=7, decimal_places=2, default=0)
    cgst_pc = models.DecimalField(max_digits=4, decimal_places=2, default=None)
    sgst_pc = models.DecimalField(max_digits=4, decimal_places=2, default=None)
    cess_pc = models.DecimalField(max_digits=4, decimal_places=2, default=None)
    user_subscription_id = models.IntegerField(null=True)
    subsidy_source = models.CharField(max_length=40, null=True)

    class Meta:
        db_table = 'order_items'
